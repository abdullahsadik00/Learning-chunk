// Q1: What's the difference between `prisma migrate dev` and `prisma migrate deploy`?
// migrate dev:
//   - For LOCAL development only
//   - Detects schema drift, generates a new migration.sql file, applies it, re-generates client
//   - Interactive: prompts you for a migration name
