console.log("=== 6. Transactions ===");

/*
  A database transaction groups multiple operations so they either ALL succeed
  or ALL fail together. This is the ACID guarantee (Atomicity).

  Without a transaction in a money transfer:
    Step 1: Debit sender's account   ← succeeds
    Step 2: Credit receiver's account ← DATABASE CRASH
    Result: Money has vanished. Sender is debited, receiver never credited.

  With a transaction:
    Step 1: Debit  (within transaction)
    Step 2: Credit (within transaction)
    CRASH → entire transaction is rolled back. Sender keeps their money.

  PRISMA OFFERS TWO TRANSACTION STYLES
  ──────────────────────────────────────

  STYLE 1: Sequential (batch) transactions
  ─────────────────────────────────────────
  Pass an array of Prisma calls. Prisma wraps them in a single transaction.
  All calls are prepared BEFORE being sent to the DB.

  const [updatedUser, newPost] = await prisma.$transaction([
    prisma.user.update({ where: { id }, data: { name: "New Name" } }),
    prisma.post.create({ data: { title: "Hello", authorId: id } }),
  ]);

  Limitation: You cannot use the result of one operation as input to the next.
  They are all constructed before any runs.

  STYLE 2: Interactive transactions
  ──────────────────────────────────
  Pass an async callback that receives a `tx` client. Operations run
  sequentially inside the transaction. You CAN read results mid-transaction.

  await prisma.$transaction(async (tx) => {
    const sender = await tx.account.findUnique({ where: { userId: senderId } });
    if (sender.balance < amount) throw new Error("INSUFFICIENT_BALANCE");

    await tx.account.update({ where: { userId: senderId }, data: { balance: { decrement: amount } } });
    await tx.account.update({ where: { userId: receiverId }, data: { balance: { increment: amount } } });
    await tx.transaction.create({ data: { senderId, receiverId, amount } });
    // If ANY line above throws, ALL three operations are rolled back
  });

  Use interactive transactions when:
    ✓ You need to READ data mid-transaction and branch on it
    ✓ You need the result of step N to construct step N+1
    ✓ You need to validate a condition (e.g., sufficient balance) mid-flight

  Use sequential transactions when:
    ✓ Operations are independent and pre-defined
    ✓ You just need atomicity without conditional logic
    ✓ You want slightly less overhead (no round-trips mid-transaction)
*/

