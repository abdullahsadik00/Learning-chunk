console.log("=== 1. What Prisma Is ===");

/*
  THREE LEVELS OF DATABASE ACCESS
  ────────────────────────────────
  Raw SQL (pg / mysql2)
    + Maximum control, maximum performance
    - No type safety, manual result typing, SQL injection risk, verbose
    e.g.  const res = await pool.query('SELECT * FROM users WHERE id = $1', [id])

  Query Builder (Knex, Drizzle)
    + Composable queries, some type safety, thin abstraction
    - You still think in SQL terms (JOIN, WHERE, SELECT)
    e.g.  knex('users').where('id', id).select('name', 'email')

  ORM — Object-Relational Mapper (Prisma, TypeORM, Sequelize)
    + Think in objects/models, not tables; relations are first-class
    + Generated TypeScript types — IDE autocomplete for every field
    + Handles migrations, seeding, studio UI
    - Slightly higher abstraction cost; complex queries may need raw fallback

  PRISMA'S THREE COMPONENTS
  ─────────────────────────
  1. Prisma Client  — the auto-generated, type-safe query builder
                      generated by: npx prisma generate
                      import { PrismaClient } from '@prisma/client'

  2. Prisma Migrate — schema migration tool
                      `prisma migrate dev`    → dev workflow (creates + applies)
                      `prisma migrate deploy` → CI/production (applies only)
                      `prisma db push`        → prototyping (no migration files)

  3. Prisma Studio  — visual GUI for browsing/editing data
                      run with: npx prisma studio
                      opens on http://localhost:5555

  HOW TYPE SAFETY WORKS
  ─────────────────────
  schema.prisma (your model definitions)
      ↓  npx prisma generate
  node_modules/@prisma/client (generated TS types)
      ↓  import into your app
  prisma.user.findUnique(...)  ← TypeScript knows every field and its type
*/

