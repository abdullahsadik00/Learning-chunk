console.log("=== 3. Relations ===");

/*
  PRISMA RELATION ANATOMY
  ────────────────────────
  Every relation requires:
    1. A relation field   (virtual — not stored in DB, just for Prisma queries)
    2. A scalar FK field  (real column stored in DB)
    3. @relation() directive linking them

  ─── ONE-TO-ONE: User → Account ─────────────────────────────
  (From paytm schema — one User has exactly one Account)

  model User {
    id      String   @id @default(cuid())
    account Account?          // ← relation field, nullable (user may have no account)
  }

  model Account {
    id     String @id @default(cuid())
    userId String @unique      // ← scalar FK — @unique enforces 1-to-1
    user   User   @relation(fields: [userId], references: [id], onDelete: Cascade)
    //           @relation tells Prisma: userId column → User.id column
    //           onDelete: Cascade → deleting User also deletes Account
  }

  ─── ONE-TO-MANY: User → Posts ──────────────────────────────
  One User can write many Posts. Each Post belongs to one User.

  model User {
    id    String @id @default(cuid())
    posts Post[]              // ← relation field (array = many)
  }

  model Post {
    id       String @id @default(cuid())
    authorId String           // ← scalar FK
    author   User   @relation(fields: [authorId], references: [id])
    //       no @unique here → one user can have many posts
  }

  ─── ONE-TO-MANY WITH NAMED RELATIONS (Paytm) ───────────────
  When a model appears TWICE in relations to the same target,
  you MUST name them to disambiguate.

  model User {
    sentTxns     Transaction[] @relation("SentTransactions")
    receivedTxns Transaction[] @relation("ReceivedTransactions")
  }

  model Transaction {
    senderId   String
    sender     User @relation("SentTransactions",   fields: [senderId],   references: [id])
    receiverId String
    receiver   User @relation("ReceivedTransactions", fields: [receiverId], references: [id])
  }

  ─── MANY-TO-MANY (IMPLICIT) ─────────────────────────────────
  Prisma creates a hidden join table automatically.
  Good for simple M:N where you don't need extra fields on the join.

  model Post {
    id   String @id @default(cuid())
    tags Tag[]
  }

  model Tag {
    id    String @id @default(cuid())
    name  String @unique
    posts Post[]
  }
  // Prisma creates _PostToTag(A: postId, B: tagId) behind the scenes

  ─── MANY-TO-MANY (EXPLICIT JUNCTION) ────────────────────────
  Use when you need extra data on the join (e.g., "assignedAt", "role").

  model Post {
    id          String       @id @default(cuid())
    postTags    PostTag[]
  }

  model Tag {
    id       String    @id @default(cuid())
    name     String    @unique
    postTags PostTag[]
  }

  model PostTag {
    postId     String
    tagId      String
    assignedAt DateTime @default(now())
    assignedBy String   // extra field — only possible with explicit junction

    post Post @relation(fields: [postId], references: [id])
    tag  Tag  @relation(fields: [tagId],  references: [id])

    @@id([postId, tagId])  // composite PK
  }

  ─── REQUIRED vs OPTIONAL RELATIONS ─────────────────────────
  model Post {
    authorId String     // required FK → author MUST exist
    author   User   @relation(fields: [authorId], references: [id])

    editorId String?    // optional FK → editor may be null
    editor   User?  @relation("PostEditor", fields: [editorId], references: [id])
  }

  ─── onDelete BEHAVIOUR ──────────────────────────────────────
  onDelete: Cascade   → delete child when parent is deleted (Account, Transaction)
  onDelete: Restrict  → prevent parent deletion if children exist (DEFAULT)
  onDelete: SetNull   → set FK to null on parent deletion (FK must be optional)
  onDelete: NoAction  → database decides (similar to Restrict but deferred)
*/

