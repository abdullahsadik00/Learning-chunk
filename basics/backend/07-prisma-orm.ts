// Q3: Why should you use `Decimal` instead of `Float` for money fields?
//
// Float is stored as IEEE 754 floating-point — it cannot represent most decimal
// fractions exactly. For example:
//   0.1 + 0.2 in IEEE 754 = 0.30000000000000004
// In a financial system this means:
//   balance 1000.00 - 0.10 might produce 999.9000000000001
//   Over millions of transactions, these rounding errors accumulate and
//   cause real money discrepancies.
//
// Decimal stores numbers as exact decimal strings with a defined precision
// and scale (e.g., Decimal(15, 2) = up to 15 total digits, 2 after decimal).
// 1000.00 - 0.10 = 999.90 exactly. Always.
//
// The Paytm schema uses: balance Decimal @db.Decimal(15, 2)
// This means balances are always stored with exactly 2 decimal places,
// supporting up to ₹9,999,999,999,999.99

const floatErr = 0.1 + 0.2;
console.log(`Q3: Float: 0.1 + 0.2 = ${floatErr} (precision error!)`);
console.log("    Decimal avoids this — exact arithmetic, critical for money.");
console.log("    Paytm schema: balance Decimal @db.Decimal(15, 2)\n");

// Q4: In a transfer operation, why must the debit and credit happen in a transaction?
//
// Without a transaction, a failure between the two operations leaves the system
// in an inconsistent state:
//
// Thread A: Debit sender (-₹500)  ← stored
//   *** Server crash / network error / exception ***
// Thread A: Credit receiver       ← NEVER executes
//
// Result: ₹500 has been destroyed. The sender is poorer, the receiver is not richer.
// No amount of retry logic can fix this safely after the fact.
//
// With a transaction (as in paytm/backend/routes/account.js):
//   The debit, credit, and Transaction record creation are ONE atomic unit.
//   If any step throws an error, the entire transaction is rolled back to the
//   state before the first operation. The DB remains consistent.
//
// Additional benefit: Prisma's interactive transaction reads the balance INSIDE
// the transaction, preventing a race condition where two concurrent transfers
// both see the old balance as sufficient and both proceed.

console.log("Q4: Without a transaction, a crash between debit and credit destroys money.");
console.log("    A transaction is atomic — all 3 ops (debit, credit, log) succeed or all fail.\n");

// Q5: What's the difference between `select` and `include` in a Prisma query?
//
// select  → pick ONLY the fields you explicitly list. Relations become
//            unavailable unless you nest a select inside.
//            { select: { firstName: true, lastName: true } }
//            Result: { firstName: "Sadik", lastName: "Shaikh" }
//            No id, no createdAt, no passwordHash.
//            Use to minimise data transferred from DB (sparse projection).
//
// include → return ALL fields of the model AND additionally load the
//            specified relations. Relations not listed are NOT loaded.
//            { include: { account: true } }
//            Result: { id, username, firstName, ..., account: { id, balance, ... } }
//            Use for eager-loading related records you need right now.
//
// They CANNOT be used together at the same query level.
// But you CAN put a select inside an include (as Paytm's account.js does):
//   include: {
//     sender:   { select: { firstName: true, lastName: true } },
//     receiver: { select: { firstName: true, lastName: true } }
//   }
// This gives you all Transaction fields + only the name fields from User.

console.log("Q5: select = sparse (only listed fields). include = all fields + relation.");
console.log("    Cannot mix at the same level. Can nest select inside include.\n");

