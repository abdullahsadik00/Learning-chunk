/*
  WHAT A MIGRATION IS
  ───────────────────
  A migration is a versioned SQL file that describes how the database schema
  changes from one state to the next. Prisma generates these automatically
  by diffing your current schema.prisma against the current DB state.

  MIGRATION FILES LIVE IN:
    prisma/migrations/
    ├── 20240101120000_init/
    │   └── migration.sql       ← auto-generated SQL
    ├── 20240102093000_add_posts/
    │   └── migration.sql
    └── migration_lock.toml     ← locks the DB provider (don't delete)

  TRACKING TABLE:
    Prisma creates `_prisma_migrations` in your DB automatically.
    It records which migrations have been applied, when, and whether they
    failed. Never manually edit this table.

  ─── npx prisma migrate dev ──────────────────────────────────
  Use during LOCAL development.

  What it does:
    1. Detects drift between schema.prisma and the current DB
    2. Generates a new migration.sql file in prisma/migrations/
    3. Applies the migration to the dev DB
    4. Re-runs `prisma generate` to update the client types
    5. Prompts for a migration name (e.g., "add_posts_table")

  Example:
    $ npx prisma migrate dev --name add_posts_table
    ✔ Generated migration 20240102_add_posts_table
    ✔ Applied migration
    ✔ Generated Prisma Client

  ─── npx prisma migrate deploy ───────────────────────────────
  Use in CI/CD and production.

  What it does:
    1. Reads pending migrations from prisma/migrations/
    2. Applies them in order — NO generation, NO prompts, NO drift detection
    3. Fails loudly if a migration has been edited after it was applied

  Key difference from `migrate dev`:
    • Never creates new migration files
    • Never generates the client
    • Safe for automated pipelines — deterministic, no user input needed

  Example in Dockerfile / CI:
    RUN npx prisma migrate deploy
    RUN node dist/server.js

  ─── npx prisma db push ──────────────────────────────────────
  Use for RAPID PROTOTYPING (schema exploration, hackathons).

  What it does:
    • Pushes schema.prisma directly to the DB WITHOUT creating migration files
    • Regenerates the Prisma Client

  Tradeoffs:
    ✓ Fast iteration — no migration files to manage
    ✗ Destructive if you remove/rename fields (no safety prompts)
    ✗ Cannot be deployed to production in a safe, repeatable way
    ✗ No migration history — can't reproduce the schema on a fresh DB

  ─── SQUASHING MIGRATIONS ────────────────────────────────────
  If you have hundreds of old migrations slowing down `migrate deploy`,
  you can squash them:

    $ npx prisma migrate squash --experimental
    # (or manually: delete old migrations, create one new "baseline" migration)

  Workflow for squashing in production:
    1. Take a DB dump as backup
    2. Delete old migration folders
    3. Run `prisma migrate resolve --applied <new_baseline_name>` on each
       existing production DB to mark it as already applied
    4. From now on, new `migrate deploy` runs only see new migrations

  ─── ROLLING BACK ────────────────────────────────────────────
  Prisma does NOT have a built-in rollback command.
  Options:
    1. Write a new "down" migration manually and apply it
    2. Restore DB from backup (safest for production disasters)
    3. Use `prisma migrate resolve --rolled-back <migration_name>` to mark
       a failed migration as rolled back in _prisma_migrations without re-running it
*/

